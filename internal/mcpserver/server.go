func (s *Server) getGroceryList(ctx context.Context, req mcp.CallToolRequest) (*mcp.CallToolResult, error) {
	items, err := s.paprika3.ListGroceryItems(ctx)
	if err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	}

	var sb strings.Builder
	sb.WriteString("## Grocery List\n\n")
	sb.WriteString("| Item | UID | Aisle | Checked |\n|---|---|---|---|\n")
	for _, item := range items {
		name := item.Name
		if name == "" {
			name = item.Ingredient
		}
		checked := " "
		if item.Purchased {
			checked = "x"
		}
		sb.WriteString(fmt.Sprintf("| %s | %s | %s | [%s] |\n", name, item.UID, item.Aisle, checked))
	}

	return mcp.NewToolResultText(sb.String()), nil
}

