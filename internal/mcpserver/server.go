func (s *Server) addGroceryItem(ctx context.Context, req mcp.CallToolRequest) (*mcp.CallToolResult, error) {
	name, _ := req.Params.Arguments["name"].(string)
	ingredient, _ := req.Params.Arguments["ingredient"].(string)
	quantity, _ := req.Params.Arguments["quantity"].(string)
	aisle, _ := req.Params.Arguments["aisle"].(string)
	recipe, _ := req.Params.Arguments["recipe"].(string)
	recipeUID, _ := req.Params.Arguments["recipe_uid"].(string)

	// Fetch existing items to copy list_uid and build aisle name → aisle_uid map.
	existing, err := s.paprika3.ListGroceryItems(ctx)
	if err != nil {
		return mcp.NewToolResultError(fmt.Sprintf("failed to fetch grocery list: %v", err)), nil
	}
	var listUID string
	aisleUIDs := make(map[string]string) // aisle name → aisle_uid
	for _, it := range existing {
		if listUID == "" {
			listUID = it.ListUID
		}
		if it.Aisle != "" && it.AisleUID != "" {
			aisleUIDs[it.Aisle] = it.AisleUID
		}
	}

	// Auto-assign aisle from map if caller didn't provide one.
	if aisle == "" {
		aisle, _ = s.aisleMap.Lookup(ingredient)
	}
	aisleUID := aisleUIDs[aisle]

	var recipePtr, recipeUIDPtr *string
	if recipe != "" {
		recipePtr = &recipe
	}
	if recipeUID != "" {
		recipeUIDPtr = &recipeUID
	}

	item := paprika.GroceryItem{
		Name:       name,
		Ingredient: ingredient,
		Quantity:   quantity,
		Aisle:      aisle,
		AisleUID:   aisleUID,
		Recipe:     recipePtr,
		RecipeUID:  recipeUIDPtr,
		ListUID:    listUID,
	}

	if err := s.paprika3.SaveGroceryItem(ctx, item); err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	}

	return mcp.NewToolResultText(fmt.Sprintf("Added '%s' to grocery list.", name)), nil
}

// aisleCandidate represents a proposed aisle change for a single item.
type aisleCandidate struct {
	displayName  string
	currentAisle string
	newAisle     string
	apply        func(ctx context.Context) error
}

// applyAisleAssignments renders a table of proposed aisle changes and optionally commits them.
// unknownNote is appended below the unknown-items list if non-empty.
func (s *Server) applyAisleAssignments(
	ctx context.Context,
	toolName string,
	dryRun bool,
	proposals []aisleCandidate,
	unknown []string,
	unknownNote string,
) (*mcp.CallToolResult, error) {
	var sb strings.Builder
	mode := "DRY RUN — no changes written"
	if !dryRun {
		mode = "APPLYING CHANGES"
	}
	sb.WriteString(fmt.Sprintf("## %s (%s)\n\n", toolName, mode))

	if len(proposals) > 0 {
		sb.WriteString("### Changes\n\n| Item | Current Aisle | → New Aisle |\n|---|---|---|\n")
		var applyErrs []string
		for _, p := range proposals {
			sb.WriteString(fmt.Sprintf("| %s | %s | %s |\n", p.displayName, p.currentAisle, p.newAisle))
			if !dryRun {
				if err := p.apply(ctx); err != nil {
					applyErrs = append(applyErrs, fmt.Sprintf("%s: %v", p.displayName, err))
				}
			}
		}
		if len(applyErrs) > 0 {
			sb.WriteString("\n### Errors\n\n")
			for _, e := range applyErrs {
				sb.WriteString(fmt.Sprintf("- %s\n", e))
			}
		}
	} else {
		sb.WriteString("All items already have correct aisle assignments.\n")
	}

	if len(unknown) > 0 {
		sb.WriteString("\n### Unknown items (not in aisle map — review manually)\n\n")
		for _, u := range unknown {
			sb.WriteString(fmt.Sprintf("- %s\n", u))
		}
		if unknownNote != "" {
			sb.WriteString("\n" + unknownNote + "\n")
		}
	}

	return mcp.NewToolResultText(sb.String()), nil
}

func setupWoodmansAislesTool() mcp.Tool {
	return mcp.NewTool("setup_woodmans_aisles",
		mcp.WithDescription("Map all grocery list items to their Woodman's East aisle. dry_run=true (default) shows proposed changes without writing. Set dry_run=false to commit. Items not found in the aisle map are flagged for manual review."),
		mcp.WithBoolean("dry_run",
			mcp.Description("If true (default), return proposed changes without writing to Paprika."),
		),
	)
}

func (s *Server) setupWoodmansAisles(ctx context.Context, req mcp.CallToolRequest) (*mcp.CallToolResult, error) {
	dryRun := true
	if v, ok := req.Params.Arguments["dry_run"].(bool); ok {
		dryRun = v
	}

	items, err := s.paprika3.ListGroceryItems(ctx)
	if err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	}

	var proposals []aisleCandidate
	var unknown []string

	for _, item := range items {
		lookupKey := item.Name
		if lookupKey == "" {
			lookupKey = item.Ingredient
		}
		aisle, found := s.aisleMap.Lookup(lookupKey)
		if !found {
			unknown = append(unknown, lookupKey)
			continue
		}
		if aisle != item.Aisle {
			captured := item
			displayName := item.Name
			if displayName == "" {
				displayName = item.Ingredient
			}
			proposals = append(proposals, aisleCandidate{
				displayName:  displayName,
				currentAisle: item.Aisle,
				newAisle:     aisle,
				apply: func(ctx context.Context) error {
					captured.Aisle = aisle
					return s.paprika3.UpdateGroceryItem(ctx, captured)
				},
			})
		}
	}

	return s.applyAisleAssignments(ctx, "setup_woodmans_aisles", dryRun, proposals, unknown,
		"To add them: update `aisles/woodmans_east.json` and re-run.")
}

